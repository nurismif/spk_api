    /**
     * Handle the user "deleted" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function deleted(User $user)
    {
        $ahp = AhpMethod::where('user_id', $user->id);
        $wp = WpMethod::where('user_id', $user->id);

        if ($ahp->first() != null) {
            $ahp->delete();
        }

        if ($wp->first() != null) {
            $wp->delete();
        }
    }

